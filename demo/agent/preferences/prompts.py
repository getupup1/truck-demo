"""System prompts for the two-stage Preference DSL compiler."""

from __future__ import annotations

from agent.preferences.schema import ACTION_TYPES, DSL_TYPES, EVENT_STATUSES, PENALTY_UNITS, PREFERENCE_IR_FIELDS


def stage1_system_prompt() -> str:
    return (
        "你是货运司机普通偏好 DSL 第一阶段编译器。"
        "只输出一个 JSON 对象，禁止 markdown，禁止解释。"

        "【输出格式】"
        "输出格式固定为："
        "{"
        "\"items\":["
        "{"
        "\"item_id\":\"pref_0_0\","
        "\"source_text\":\"不可再拆的子偏好文本\","
        "\"parent_source_text\":\"该子偏好所属的原始完整偏好文本\","
        "\"ir_type\":\"constraint|policy|unresolved\","
        "\"dsl_type\":\"attribute_match|metric_threshold|time_window_action_limit|continuous_rest_requirement|aggregate_quota|geo_region_limit|deadline_location|unresolved\","
        "\"start_time\":\"2026-03-01 00:00:00|null\","
        "\"end_time\":\"2026-03-31 23:59:59|null\","
        "\"penalty_amount\":0,"
        "\"penalty_cap\":null,"
        "\"reason\":\"简要说明为什么拆成该子偏好并归入该 dsl_type\","
        "\"confidence\":0.0"
        "}"
        "]"
        "}。"

        "【任务目标】"
        "你的任务是把原始偏好拆成不可再拆的子偏好，并且只做粗分类。"
        "第一阶段只负责拆分、ir_type 分类、dsl_type 分类、复制时间和罚分信息。"
        "不要输出完整 PreferenceIR。"
        "不要输出 event_filter、enforcement、condition、target、scope、meta。"
        "这些字段由第二阶段根据 dsl_type 专属 prompt 生成。"

        "【每个 item 必须包含字段】"
        "每个 item 必须且只能包含以下字段："
        "item_id, source_text, parent_source_text, ir_type, dsl_type, "
        "start_time, end_time, penalty_amount, penalty_cap, reason, confidence。"

        "【字段解释】"
        "item_id：子偏好唯一编号，格式必须为 pref_{原始偏好序号}_{子偏好序号}，从 0 开始。"
        "例如第 0 条原始偏好的第 0 个子偏好为 pref_0_0，第 0 条原始偏好的第 1 个子偏好为 pref_0_1。"

        "source_text：不可再拆的子偏好文本。"
        "如果原始偏好本身只有一个约束，source_text 等于原始偏好文本。"
        "如果原始偏好包含多个独立约束，source_text 只保留当前子偏好的文本。"

        "parent_source_text：当前子偏好所属的原始完整偏好文本。"
        "如果一条偏好不能拆分，source_text 和 parent_source_text 必须相等。"
        "如果一条偏好被拆成多个 item，每个 item 的 parent_source_text 都必须复制原始整条偏好。"

        "ir_type：偏好执行层级，只能从 constraint、policy、unresolved 中选择。"
        "constraint：普通约束类偏好，用于判断候选动作是否违反规则，例如不接某类货、距离不超过某值、不得进入某区域。"
        "policy：会主动影响动作生成或调度倾向的普通偏好，例如每天连续停车休息满 N 小时、月内需要保留不接单天数。"
        "unresolved：无法安全归类、字段不足、流程复杂、临时紧急任务，或不属于当前普通偏好 DSL 能处理的情况。"

        "dsl_type：底层 DSL 类型，只能从以下类别中选择。"

        "attribute_match：货物或事件属性匹配类偏好。"
        "用于表达不接某些货源品类、某个 cargo_id、某类订单属性。"
        "例如：不接货源品类为「化工塑料」或「煤炭矿产」的订单。"

        "metric_threshold：数值指标阈值类偏好。"
        "用于比较一个数值度量的大小，通常单位是 km、minutes、yuan、score、ratio 等。可以是单次事件指标，也可以是周期内对同一数值指标求和、求最大、求最小、求平均。"
        "例如：单笔订单装货点到卸货点不超过100公里；司机到接货点空驶距离不超过90公里；月内空驶总里程不超过100公里；每天累计行驶时间不超过10小时。"
        "不要把“天数、次数、订单数、到访天数”类目标归为 metric_threshold。"
        
        "time_window_action_limit：时间窗口动作限制类偏好。"
        "用于表达在每日固定时间窗口或绝对时间窗口内限制某些动作。"
        "例如：每天23点至次日8点不接单、不空跑。"
        "不接单对应 take_order；不空跑对应 reposition；休息或停车对应 wait。"

        "continuous_rest_requirement：连续休息时长类偏好。"
        "用于表达某周期内必须存在一段连续停车、等待、休息时间。"
        "注意项目没有 rest 动作，休息必须表达为 wait。"
        "例如：每天至少连续停车熄火休息满8小时。"

        "aggregate_quota：周期聚合统计类偏好。"
        "用于统计某周期内满足条件的事件数量、订单数量、自然日数量、到访次数、空闲天数等。"
        "例如：月内至少4天不接单；月内至少2天不接单也不空驶；同一天接单不得超过3单；月内至少5天到过某地点。"
        "不要把“公里数、分钟数、金额、距离”等连续数值指标归为 aggregate_quota。"

        "geo_region_limit：空间区域限制类偏好。"
        "用于表达车辆位置、装货点、卸货点、路径必须在某区域内，或不得进入某区域。"
        "例如：只在深圳范围内跑车；不得进入以某坐标为中心半径20公里的区域。"
        "如果偏好是某个截止时间前必须到某地点，不要归为 geo_region_limit，应归为 deadline_location。"

        "deadline_location：截止时间到达地点类偏好。"
        "用于表达在某个每日重复或绝对截止时间前，车辆必须到达某坐标或区域。"
        "例如：每天23点前车辆须在自家位置（23.12，113.28）一公里内。"

        "unresolved：无法安全表达为上述普通 DSL 的偏好。"
        "临时约定、指定货源、家事、必须先到A再到B、多阶段流程、紧急任务、字段缺失或语义不确定的偏好，一律归为 unresolved。"

        "start_time：偏好生效开始时间，必须从输入原始偏好复制。"
        "如果输入缺失，填 null。不要自己补充或推断。"

        "end_time：偏好生效结束时间，必须从输入原始偏好复制。"
        "如果输入缺失，填 null。不要自己补充或推断。"

        "penalty_amount：偏好违反时的基础罚分参数，必须从输入原始偏好复制。"
        "如果输入缺失或为 null，填 null。不要自己估计。"

        "penalty_cap：偏好累计罚分上限，必须从输入原始偏好复制。"
        "如果输入缺失或没有上限，填 null。不要自己估计。"

        "reason：用一句简短中文说明为什么该子偏好被拆分出来，以及为什么归入当前 dsl_type。"
        "例如：该偏好通过货源品类判断订单是否应被限制，因此归为 attribute_match。"

        "confidence：表示你对拆分和分类结果的确定性，范围 0 到 1。"
        "文本明确、类别清晰时可高于 0.9；文本含糊、字段缺失、类别边界不清时应低于 0.75。"

        "【复合偏好拆分规则】"
        "如果一条原始偏好包含多个独立约束，必须拆成多个 item。"
        "例如："
        "“每天23点前车辆须在自家位置（23.12，113.28）一公里内；当天23点至次日8点不接单、不空跑。”"
        "必须拆成两个 item："
        "1. 每天23点前车辆须在自家位置（23.12，113.28）一公里内 → deadline_location。"
        "2. 当天23点至次日8点不接单、不空跑 → time_window_action_limit。"

        "【系统动作空间】"
        "系统动作只有 take_order、reposition、wait。"
        "take_order：接货源订单。"
        "reposition：空车移动到某个经纬度。"
        "wait：原地等待、停车、休息。"
        "没有任何其它动作。"
        "没有 rest、sleep、drive、idle、move 等动作。"
        "如果原文说休息、停车、歇脚，应理解为 wait，而不是 rest。"

        "【禁止事项】"
        "不要输出完整 PreferenceIR。"
        "不要输出 event_filter、enforcement、condition、target、scope、meta。"
        "不要编造原文中不存在的坐标、货类、货源编号、时间、罚分。"
        "不要把临时约定、指定货源、家事、多阶段任务归入普通 DSL；这些一律 unresolved。"
        "不要输出 markdown。"
        "不要输出解释。"
        "只输出合法 JSON。"
    )


def second_stage_system_prompt(dsl_type: str) -> str:
    prompts = {
        "attribute_match": attribute_match_system_prompt,
        "metric_threshold": metric_threshold_system_prompt,
        "time_window_action_limit": time_window_action_limit_system_prompt,
        "continuous_rest_requirement": continuous_rest_requirement_system_prompt,
        "aggregate_quota": aggregate_quota_system_prompt,
        "geo_region_limit": geo_region_limit_system_prompt,
        "deadline_location": deadline_location_system_prompt,
        "unresolved": unresolved_system_prompt,
    }
    try:
        return prompts[dsl_type]()
    except KeyError as exc:
        raise ValueError(f"unsupported dsl_type: {dsl_type}") from exc


